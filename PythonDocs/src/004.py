a = 20
b = 10

# if结构
if a > b:
    print("yes")

# if...else结构
if a > b:
    print("yes")
else:
    print("no")

# if...elif结构
if a > b:
    print("yes")
elif b > 0:
    print("???")
else:
    print("no")

# 嵌套分支
if a > b:
    if a > 21:
        print("yes")
    elif b > 0:
        print("???")
else:
    print("no")
