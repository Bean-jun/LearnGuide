package chapter03;

public class IfElse {
    public static void main(String[] args) {
        int a = 0;
        if (a > 2) {
            System.out.println("a>2");
        } else if (a > 1) {
            System.out.println("1<a<=2");
        } else {
            System.out.println("a<=1");
        }
    }
}
